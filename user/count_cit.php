<h2><a>1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Scholarly writing, plagiarism and copyright</a></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Part of what it means to be a ‘scholar’ is to engage with the work of others, for example, to extend or refine one’s own ideas, critique the work of others, or test and extend theories. Learning and assessment activities therefore often require you to gather information from a number of sources. This may include factual information, data or calculations, visual artefacts and written texts. Whenever you use the work of another person, you must acknowledge that source using the correct referencing system. Failure to acknowledge other people’s work appropriately may be regarded as plagiarism or academic misconduct. Use of one's own previous work in satisfaction of a new assessment requirement is also not allowed unless it is an explicit requirement for the unit.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>VU deals with plagiarism according to the <a href="https://policy.vu.edu.au/document/view.php?id=27">Academic Integrity and Preventing Plagiarism Policy</a>.</p>
